USE Surveys
GO

IF OBJECT_ID (N'dbo.Surveys', N'U') IS NOT NULL
	DROP TABLE dbo.Surveys;  
GO

CREATE TABLE dbo.Surveys(
	Id NVARCHAR(50),
	Name NVARCHAR(50) NOT NULL,
	Birthdate DATE NOT NULL,
	TeamId INT NOT NULL,
	Lat DECIMAL(18,4) NOT NULL,
	Lon DECIMAL(18,4) NOT NULL,
	CONSTRAINT PK_ID_SURVEYS PRIMARY KEY (Id)
);
GO